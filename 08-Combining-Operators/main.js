var result = 10 + 10 - 10 * 10 / 10;
console.log(result); // 10
// now use combing operators with prefix and postfix
var result1 = 10 + 10;
result1 = ++result1;
console.log(result1); // 20
var result2 = 10 - 10;
result2 = --result2;
console.log(result2); // 0
var result3 = 10 * 10;
result3 = result3++;
console.log(result3); // 100
var result4 = 10 / 10;
result4 = result4--;
console.log(result4); // 1

let result = 10 + 10 - 10 * 10 / 10
console.log(result)  // 10


// now use combing operators with prefix and postfix

let result1 = 10 + 10
result1 = ++result1
console.log(result1)  // 21
let result2 = 10 - 10
result2 = --result2
console.log(result2)  //-1
let result3 = 10 * 10
result3 = result3++
console.log(result3)  // 100
let result4 = 10 / 10
result4 = result4--
console.log(result4)  // 1
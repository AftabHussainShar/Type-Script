// 1: Addition 
var x = 10 + 10; // it will give 20
console.log(x);
// 2: Subtraction
var y = 10 - 10; // it will give 0
console.log(y);
// 3: Multiplication
var z = 10 * 10; // it will give 100
console.log(z);
// 4: Division
var a = 10 / 10; // it will give 1
console.log(a);
// 5: Exponnents
var b = Math.pow(10, 10); // it will give 100000000000
console.log(b);
// 6: Modulus
var c = 10 % 10; // it will give 0
console.log(c);
